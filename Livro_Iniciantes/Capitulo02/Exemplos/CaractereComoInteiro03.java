// vari치veis do tipo caractere tratada como inteiros
class CaractereComoInteiro
{
	public static void main(String[] args)
	{
		// vari치vel
		char letra = 'X';
		
		System.out.println("char letra = " + letra );
		
		letra++; // incrementa vari치vel
		
		System.out.println("char letra++ -> " + letra );
		
		letra = 90; // vari치vel char recebe valor inteiros
		
		System.out.println("char letra = 90 -> " + letra );
		
		
	} // fim main
} // fim class